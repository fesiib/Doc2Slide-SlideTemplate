function objRecTraverse(obj, prefix = '') {
    if (typeof obj !== 'object' || obj === null) {
        return [];
    }
    let fields = [];
    for (let field in obj) {
        fields.push(prefix + '.' + field);
        fields = fields.concat(objRecTraverse(obj[field], prefix + '.' + field));
    }
    return fields;
}

